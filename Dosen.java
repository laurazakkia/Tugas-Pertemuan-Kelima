    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String nomor_induk_dosen, nama_dosen, tanggal_lahir, jenis_kelamin, alamat, email;
        if (tfNID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfNamaDosen.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfTanggal.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJenkel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                String sql = "UPDATE Dosen SET nama_dosen = ?, tanggal_lahir = ?, jenis_kelamin = ?, alamat = ?, email = ? WHERE nomor_induk_dosen = ?";
                conn = DriverManager.getConnection(koneksi, user, password);
                pstmt = conn.prepareStatement(sql);

                nomor_induk_dosen = tfNID.getText();
                nama_dosen = tfNamaDosen.getText();
                tanggal_lahir = tfTanggal.getText();
                jenis_kelamin = tfJenkel.getText();
                alamat = tfAlamat.getText();
                email = tfEmail.getText();

                pstmt.setString(1, nama_dosen);
                // Pastikan format tanggal_lahir sesuai dengan format database
                pstmt.setDate(2, java.sql.Date.valueOf(tanggal_lahir));
                pstmt.setString(3, jenis_kelamin);
                pstmt.setString(4, alamat);
                pstmt.setString(5, email);
                pstmt.setString(6, nomor_induk_dosen);

                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
                    pstmt.close();
                    conn.close();
                    bersih();
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Dosen.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
        showTable();

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:

        if (tfNID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfNamaDosen.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfTanggal.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJenkel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                conn = DriverManager.getConnection(koneksi, user, password);
                conn.setAutoCommit(false);

                String sql = "INSERT INTO Dosen VALUES(?,?,?,?,?,?)";
                pstmt = conn.prepareStatement(sql);

                String nomor_induk_dosen, nama_dosen, tanggal_lahir, jenis_kelamin, alamat, email;
                nomor_induk_dosen = tfNID.getText();
                nama_dosen = tfNamaDosen.getText();
                tanggal_lahir = tfTanggal.getText();
                jenis_kelamin = tfJenkel.getText();
                alamat = tfAlamat.getText();
                email = tfEmail.getText();

                pstmt.setString(1, nomor_induk_dosen);
                pstmt.setString(2, nama_dosen);
                pstmt.setDate(3, java.sql.Date.valueOf(tanggal_lahir)); // Pastikan formatnya benar
                pstmt.setString(4, jenis_kelamin);
                pstmt.setString(5, alamat);
                pstmt.setString(6, email);

                pstmt.executeUpdate();
                conn.commit();
                pstmt.close();
                conn.close();

                JOptionPane.showMessageDialog(null, "Sukses diinput");
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat pengisianse");
            }
        }
        showTable();
    }//GEN-LAST:event_btnTambahActionPerformed

